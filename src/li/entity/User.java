package li.entity;



public class User {
	public int id;
	public int uid;
	public String name;
	public String passwrod;
	public String head_image_url;
	public boolean atuo;
	public boolean fist;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the uid
	 */
	public int getUid() {
		return uid;
	}
	/**
	 * @param uid the uid to set
	 */
	public void setUid(int uid) {
		this.uid = uid;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the passwrod
	 */
	public String getPasswrod() {
		return passwrod;
	}
	/**
	 * @param passwrod the passwrod to set
	 */
	public void setPasswrod(String passwrod) {
		this.passwrod = passwrod;
	}
	/**
	 * @return the head_image_url
	 */
	public String getHead_image_url() {
		return head_image_url;
	}
	/**
	 * @param head_image_url the head_image_url to set
	 */
	public void setHead_image_url(String head_image_url) {
		this.head_image_url = head_image_url;
	}
	/**
	 * @return the atuo
	 */
	public boolean isAtuo() {
		return atuo;
	}
	/**
	 * @param atuo the atuo to set
	 */
	public void setAtuo(boolean atuo) {
		this.atuo = atuo;
	}
	/**
	 * @return the fist
	 */
	public boolean isFist() {
		return fist;
	}
	/**
	 * @param fist the fist to set
	 */
	public void setFist(boolean fist) {
		this.fist = fist;
	}
	

}
